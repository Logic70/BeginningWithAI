"""
Experiment 2.0b: build a WorkflowProgram RAG context pack for Claude Code.

Goal:
  Turn a local WorkflowProgram repository or documentation directory into a
  small retrieval context that Claude Code can consume through an @file mention.

This is not a vector database. It is the final Phase 2 RAG exercise: prove that
we can retrieve project-system knowledge and export a compact, source-linked
context package for a coding agent.

Run with any local source directory first:
  ./venv/bin/python experiments/phase2/exp2_0b_workflowprogram_rag_for_claudecode.py \
    --source-root docs \
    --query "Subagent Runtime 和 Agent Team 有什么区别？"

Run with WorkflowProgram when the repository path is available:
  ./venv/bin/python experiments/phase2/exp2_0b_workflowprogram_rag_for_claudecode.py \
    --source-root D:/Code/WorkflowProgram \
    --query "WorkflowProgram 的核心对象模型是什么？" \
    --export data/rag/workflowprogram_claude_context.md
"""

from __future__ import annotations

import argparse
import math
import re
from collections import Counter
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable


PROJECT_ROOT = Path(__file__).resolve().parents[2]
DEFAULT_EXPORT = PROJECT_ROOT / "data" / "rag" / "workflowprogram_claude_context.md"

TEXT_EXTENSIONS = {
    ".md",
    ".mdx",
    ".txt",
    ".py",
    ".ts",
    ".tsx",
    ".js",
    ".jsx",
    ".json",
    ".yaml",
    ".yml",
    ".toml",
}

IGNORE_DIRS = {
    ".git",
    ".idea",
    ".vscode",
    "__pycache__",
    "node_modules",
    "dist",
    "build",
    ".next",
    ".venv",
    "venv",
    "wsl_venv",
    "venv_finetune",
}


@dataclass
class SourceChunk:
    path: Path
    rel_path: str
    start_line: int
    end_line: int
    text: str


@dataclass
class ScoredSourceChunk:
    chunk: SourceChunk
    score: float


def iter_source_files(root: Path) -> Iterable[Path]:
    for path in root.rglob("*"):
        if not path.is_file():
            continue
        if any(part in IGNORE_DIRS for part in path.parts):
            continue
        if path.suffix.lower() not in TEXT_EXTENSIONS:
            continue
        yield path


def read_text_safely(path: Path) -> str:
    try:
        return path.read_text(encoding="utf-8")
    except UnicodeDecodeError:
        return path.read_text(encoding="utf-8", errors="ignore")


def chunk_file(path: Path, root: Path, lines_per_chunk: int, overlap: int) -> list[SourceChunk]:
    lines = read_text_safely(path).splitlines()
    chunks: list[SourceChunk] = []
    start = 0
    rel_path = path.relative_to(root).as_posix()
    while start < len(lines):
        end = min(start + lines_per_chunk, len(lines))
        text = "\n".join(lines[start:end]).strip()
        if text:
            chunks.append(
                SourceChunk(
                    path=path,
                    rel_path=rel_path,
                    start_line=start + 1,
                    end_line=end,
                    text=text,
                )
            )
        if end == len(lines):
            break
        start = max(0, end - overlap)
    return chunks


def tokenize(text: str) -> list[str]:
    return [token.lower() for token in re.findall(r"[\u4e00-\u9fff]|[A-Za-z0-9_./:-]+", text)]


def vectorize(text: str) -> Counter[str]:
    return Counter(tokenize(text))


def cosine_similarity(left: Counter[str], right: Counter[str]) -> float:
    if not left or not right:
        return 0.0
    dot = sum(value * right.get(key, 0) for key, value in left.items())
    left_norm = math.sqrt(sum(value * value for value in left.values()))
    right_norm = math.sqrt(sum(value * value for value in right.values()))
    if left_norm == 0 or right_norm == 0:
        return 0.0
    return dot / (left_norm * right_norm)


def build_chunks(source_root: Path, lines_per_chunk: int, overlap: int) -> list[SourceChunk]:
    chunks: list[SourceChunk] = []
    for file_path in iter_source_files(source_root):
        chunks.extend(chunk_file(file_path, source_root, lines_per_chunk, overlap))
    return chunks


def retrieve(query: str, chunks: list[SourceChunk], top_k: int) -> list[ScoredSourceChunk]:
    query_vector = vectorize(query)
    scored = [
        ScoredSourceChunk(chunk=chunk, score=cosine_similarity(query_vector, vectorize(chunk.text)))
        for chunk in chunks
    ]
    scored.sort(key=lambda item: item.score, reverse=True)
    return [item for item in scored[:top_k] if item.score > 0]


def format_context(query: str, source_root: Path, results: list[ScoredSourceChunk]) -> str:
    lines = [
        "# WorkflowProgram RAG Context For Claude Code",
        "",
        "This file is generated by `experiments/phase2/exp2_0b_workflowprogram_rag_for_claudecode.py`.",
        "Use it as a source-grounded context pack for Claude Code by mentioning this file in the prompt.",
        "",
        f"Source root: `{source_root}`",
        f"Query: `{query}`",
        "",
        "## Retrieved Evidence",
        "",
    ]

    if not results:
        lines.append("No relevant chunks were retrieved.")
        return "\n".join(lines)

    for index, result in enumerate(results, start=1):
        chunk = result.chunk
        lines.extend(
            [
                f"### {index}. `{chunk.rel_path}` lines {chunk.start_line}-{chunk.end_line}",
                "",
                f"Score: `{result.score:.4f}`",
                "",
                "```text",
                chunk.text[:4000],
                "```",
                "",
            ]
        )

    lines.extend(
        [
            "## Claude Code Prompt Pattern",
            "",
            "```text",
            "请基于 @data/rag/workflowprogram_claude_context.md 中的证据回答问题。",
            "如果证据不足，请明确说明缺少哪类文件或上下文，不要编造 WorkflowProgram 的实现。",
            "```",
            "",
        ]
    )
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(description="Build WorkflowProgram RAG context for Claude Code")
    parser.add_argument("--source-root", type=Path, required=True, help="WorkflowProgram repo/docs root")
    parser.add_argument("--query", required=True, help="Question to retrieve context for")
    parser.add_argument("--top-k", type=int, default=6)
    parser.add_argument("--lines-per-chunk", type=int, default=80)
    parser.add_argument("--overlap", type=int, default=12)
    parser.add_argument("--export", type=Path, default=DEFAULT_EXPORT)
    args = parser.parse_args()

    source_root = args.source_root.resolve()
    if not source_root.exists() or not source_root.is_dir():
        raise SystemExit(f"source root not found or not a directory: {source_root}")

    chunks = build_chunks(source_root, args.lines_per_chunk, args.overlap)
    results = retrieve(args.query, chunks, args.top_k)
    context = format_context(args.query, source_root, results)

    export_path = args.export
    if not export_path.is_absolute():
        export_path = PROJECT_ROOT / export_path
    export_path.parent.mkdir(parents=True, exist_ok=True)
    export_path.write_text(context, encoding="utf-8")

    print("=" * 60)
    print("WorkflowProgram RAG context exported")
    print("=" * 60)
    print(f"source_root: {source_root}")
    print(f"indexed_chunks: {len(chunks)}")
    print(f"retrieved_chunks: {len(results)}")
    print(f"export: {export_path}")
    print("\nTop results:")
    for result in results:
        chunk = result.chunk
        print(f"- {result.score:.4f} {chunk.rel_path}:{chunk.start_line}-{chunk.end_line}")


if __name__ == "__main__":
    main()
